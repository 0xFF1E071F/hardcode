sub Seq
{
   my($seqno,$seqdata)=@_;
   my($seqstr)='';
   my($lgt)=0;

   foreach(split('',$seqdata))
   {
         if($_ eq ',') { $seqstr.='255,'; }
      elsif($_ eq '.') { $seqstr.='254,'; }
      else             { $seqstr.=$_.','; }
      $lgt++;
   }

   print $seqno.','.$lgt.','.$seqstr."\n";
}

sub Notes
{
   print '4,'.($#_+1),',';

   foreach(@_) { print $_,','; }
   print "\n";
}

