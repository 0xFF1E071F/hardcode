{
	int i;
	static unsigned int CrtParms[]=
	 { 0xd06,0x3e07,0xc009,0xea10,0xac11,0xdf12,0x0014,0xe715,0x616,
		 0xe317 };
	_AX=0x13;
	geninterrupt(0x10);//}
	outport(0x3c4,0x604);
	outport(0x3c4,0x100);
	outportb(0x3c2,0xe3/*0xe7*/);
	outport(0x3c4,0x300);
	outportb(0x3d4,0x11);
	outportb(0x3d5,inportb(0x3d5)&0x7f);
	for (i=0;i<10;i++) outport(0x3d4,CrtParms[i]);
	clrscr();
}
